from networkx_traffic_sim import *

def template_2input_1sink():
    m = Map(confirmation_messages = False)
    m.add_inter(1, (0,0))
    m.add_inter(2, (1,0))
    m.add_inter(3, (0,1))
    m.add_inter(4, (1,1))
    m.add_inter("ralphs", (2,1))
    m.add_road(1, 3, dt=1, speed_limit=50, length=300, lanes=2, num_cars=0)
    m.add_road(1, 4, dt=1, speed_limit=50, length=100, lanes=1, num_cars=0)
    m.add_road(3, 4, dt=1, speed_limit=50, length=100, lanes=1, num_cars=0)
    m.add_road(2, 4, dt=1, speed_limit=50, length=100, lanes=1, num_cars=0)
    m.add_road(4, "ralphs", dt=1, speed_limit=50, length=100, lanes=1, num_cars=0)
    m.declare_inflow_node(1, {'ralphs': 40})
    m.declare_inflow_node(2, {'ralphs': 80})
    m.simulation_check_compile()
    return m

